def check_word_line():
    word = "learning"

    with open("practic.txt", "r") as f:
        found = False
        for i, line in enumerate(f, start=1):
            if word in line:
                print(f"'{word}' line {i} mein mila hai.")
                found = True

        if not found:
            print(f"'{word}' kisi bhi line mein nahi mila.")

